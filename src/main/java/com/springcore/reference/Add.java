package com.springcore.reference;

public class Add {
	
	private int a;
	private Substract obj;
	
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	public Substract getObj() {
		return obj;
	}
	public void setObj(Substract obj) {
		this.obj = obj;
	}
	@Override
	public String toString() {
		return "Add [a=" + a + ", obj=" + obj + "]";
	}
	
	
	
	

}
